    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'image' => 'required|image|mimes:jpeg,png,jpg,gif,svg|max:2048',
        ]);

        $imageName = time().'.'.$request->image->extension();
        $request->image->move(public_path('images'), $imageName);

        $prod = new Products();
        $prod->prod_img = $imageName;
        $prod->price = $request->price;
        $prod->ref_code = $request->ref_code;
        $prod->prod_name = $request->prod_name;
        $prod->prod_description = $request->prod_description;
        $prod->prod_rating = $request->prod_rating;
        $prod->save();

        return view('shop',['product' => DB::table('products')->get()]);
    }

